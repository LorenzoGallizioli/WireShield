package com.wireshield.enums;

/**
 * The runningStates enum represents the possible states of Download Manager and
 * Antivirus Manager process. It is used to indicate whether a process or system
 * component is operational or not.
 */
public enum runningStates {
	UP, // Indicates that the process or system is operational and running.
	DOWN, // Indicates that the process or system is not operational or has been stopped.
}
